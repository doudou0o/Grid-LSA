package struct;

public class point {
	public int value;
	public int ID;
	public double xcoordinate;
	public double ycoordinate;

	public int getID() {
		return ID;
	}
	
	public int getValue() {
		return value;
	}

	public double getXcoordinate() {
		return xcoordinate;
	}

	public double getYcoordinate() {
		return ycoordinate;
	}
}
